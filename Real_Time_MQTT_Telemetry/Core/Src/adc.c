float readTemperature(void){
	uint32_t raw_adc = 0;
	float temperature = 0.0f;
	HAL_ADC_Start(&hadc1);
	if (HAL_ADC_PollForConversion(&hadc1, 100) == HAL_OK){
		raw_adc = HAL_ADC_GetValue(&hadc1);
	}
	HAL_ADC_Stop(&hadc1);

	float vSensed = (ADC_REFERENCE_VOLTAGE_MV * raw_adc)/ADC_MAX_OUTPUT_VALUE;
	temperature = ((vSensed - TEMP_SENSOR_VOLTAGE_MV_AT_25) / TEMP_SENSOR_AVG_SLOPE_MV_PER_CELSIUS) + 25.0f;
    return temperature;
}

